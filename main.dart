class MapPage extends StatefulWidget {
  const MapPage({super.key});

  @override
  State<MapPage> createState() => _MapPageState();
}

class _MapPageState extends State<MapPage> {
  bool isManualSelection = false; // هل المستخدم فعل وضع التحديد اليدوي؟
  LatLng? pickedLocation; // مكان الدبوس اللي بيختاره المستخدم
  GoogleMapController? _mapController;
  // دالة تفتح خرائط جوجل وتوجهك لمكان الدبوس
  Future<void> _launchMaps() async {
    if (pickedLocation != null) {
      final String url =
          "https://www.google.com/maps/dir/?api=1&destination=${pickedLocation!.latitude},${pickedLocation!.longitude}&travelmode=driving";
      final Uri uri = Uri.parse(url);
      if (await canLaunchUrl(uri)) {
        await launchUrl(uri, mode: LaunchMode.externalApplication);
      }
    }
  }

  // قائمة المواقع السياحية (الدبابيس)
  final Set<Marker> _southMarkers = {
    // --- معالم وأماكن سياحية (باللون الأخضر) ---
    Marker(
      markerId: const MarkerId('soudah'),
      position: const LatLng(18.2671, 42.3681),
      infoWindow: const InfoWindow(
        title: 'منتزه السودة',
        snippet: 'أعلى قمة في المملكة',
      ),
      icon: BitmapDescriptor.defaultMarkerWithHue(BitmapDescriptor.hueGreen),
    ),
    Marker(
      markerId: const MarkerId('almaz'),
      position: const LatLng(18.1945, 42.3353),
      infoWindow: const InfoWindow(
        title: 'رجال ألمع',
        snippet: 'قرية تراثية عالمية',
      ),
      icon: BitmapDescriptor.defaultMarkerWithHue(BitmapDescriptor.hueGreen),
    ),

    // --- مطاعم (باللون الأحمر) ---
    Marker(
      markerId: const MarkerId('res_abha'),
      position: const LatLng(18.2331, 42.5112),
      infoWindow: const InfoWindow(
        title: 'مطعم حنيذ الجنوب',
        snippet: 'أكلات شعبية أصيلة',
      ),
      icon: BitmapDescriptor.defaultMarkerWithHue(BitmapDescriptor.hueRed),
    ),
    Marker(
      markerId: const MarkerId('res_taste'),
      position: const LatLng(18.2250, 42.5100),
      infoWindow: const InfoWindow(
        title: 'مطعم مذاق الجنوب',
        snippet: 'ألذ الأطباق الجنوبية',
      ),
      icon: BitmapDescriptor.defaultMarkerWithHue(BitmapDescriptor.hueRed),
    ),

    // --- مقاهي (باللون الأزرق) ---
    Marker(
      markerId: const MarkerId('cafe_cloud'),
      position: const LatLng(18.2132, 42.5050),
      infoWindow: const InfoWindow(
        title: 'مقهى السحاب',
        snippet: 'إطلالة خيالية على مدينة أبها',
      ),
      icon: BitmapDescriptor.defaultMarkerWithHue(BitmapDescriptor.hueAzure),
    ),
    Marker(
      markerId: const MarkerId('cafe_sky'),
      position: const LatLng(18.2410, 42.5220),
      infoWindow: const InfoWindow(
        title: 'مقهى ومحمصة سكاى',
        snippet: 'قهوة مختصة وجلسات رائعة',
      ),
      icon: BitmapDescriptor.defaultMarkerWithHue(BitmapDescriptor.hueAzure),
    ),
  };

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: const Text('خريطة سكة الجنوب السياحية'),
        backgroundColor: const Color.fromARGB(255, 19, 54, 21),
      ),
      body: GoogleMap(
        onMapCreated: (controller) {
          _mapController = controller;
          // هذا الكود يخلي الخريطة تروح لموقعك فوراً
          Geolocator.getCurrentPosition().then((pos) {
            _mapController?.animateCamera(
              CameraUpdate.newLatLng(LatLng(pos.latitude, pos.longitude)),
            );
          });
        },
        initialCameraPosition: const CameraPosition(
          target: LatLng(18.2166, 42.5000), // تبدأ الخريطة من أبها
          zoom: 10.0,
        ),
        myLocationEnabled: true, // يظهر الدائرة الزرقاء لموقعك
        myLocationButtonEnabled: true,
        onTap: (position) {
          if (isManualSelection) {
            setState(() {
              pickedLocation = position;
            });
          }
        }, // يظهر زر العودة لموقعك
        markers: {
          ..._southMarkers,
          if (pickedLocation != null)
            Marker(
              markerId: const MarkerId('manual_pin'),
              position: pickedLocation!,
              icon: BitmapDescriptor.defaultMarkerWithHue(
                  BitmapDescriptor.hueAzure),
              infoWindow: InfoWindow(
                title: 'موقعي المختار',
                snippet: 'اضغط هنا للذهاب للموقع عبر Google Maps',
                onTap: () {
                  _launchMaps(); // <--- هذا السطر ينادي الدالة اللي تفتح الخريطة
                },
              ),
            ),
        },
      ),
      floatingActionButton: FloatingActionButton.extended(
        onPressed: () {
          setState(() {
            isManualSelection = !isManualSelection;
            if (!isManualSelection) pickedLocation = null;
          });
        },
        label: Text(isManualSelection ? 'إلغاء التحديد' : 'تحديد يدوي'),
        icon: Icon(isManualSelection ? Icons.close : Icons.pin_drop),
        backgroundColor: isManualSelection ? Colors.red : Colors.green,
      ),
    );
  }
}

