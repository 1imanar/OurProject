class CityDetailsPage extends StatelessWidget {
  final Map<String, dynamic> city;
  CityDetailsPage({required this.city});

  Future<void> _openGoogleMap(String query) async {
    final String encodedQuery = Uri.encodeComponent(query);
    final Uri url = Uri.parse(
      "https://www.google.com/maps/search/?api=1&query=$encodedQuery",
    );
    if (!await launchUrl(url, mode: LaunchMode.externalApplication)) {
      throw 'Could not launch $url';
    }
  }

  @override
  Widget build(BuildContext context) {
    return DefaultTabController(
      length: 4,
      child: Scaffold(
        appBar: AppBar(
          title: Text(
            city['name']!,
            style: const TextStyle(
              color: Colors.white,
              fontFamily: 'Tajawal',
              fontWeight: FontWeight.bold,
            ),
          ),
          centerTitle: true,
          backgroundColor: const Color.fromARGB(255, 19, 54, 21),
          iconTheme: const IconThemeData(color: Colors.white),
          bottom: TabBar(
            isScrollable: true,
            labelColor: Colors.amber,
            unselectedLabelColor: Colors.white,
            indicatorColor: Colors.amber,
            tabs: [
              Tab(text: '🏙 أماكن سياحية'),
              Tab(text: '🥘 مطاعم'),
              Tab(text: '☕️ مقاهي'),
              Tab(text: '🏛 معالم أثرية'),
            ],
          ),
        ),
        body: TabBarView(
          children: [
            _buildList(city['places'], 'أماكن سياحية'),
            _buildList(city['places'], 'مطاعم'),
            _buildList(city['places'], 'مقاهي'),
            _buildList(city['places'], 'معالم أثرية'),
          ],
        ),
      ),
    );
  }

  Widget _buildList(List<dynamic> places, String type) {
    final filtered = places.where((p) => p['type'] == type).toList();
    return ListView.builder(
      padding: const EdgeInsets.all(10),
      itemCount: filtered.length,
      itemBuilder: (context, index) {
        final place = filtered[index];
        return Card(
          margin: const EdgeInsets.only(bottom: 15),
          shape: RoundedRectangleBorder(
            borderRadius: BorderRadius.circular(15),
          ),
          elevation: 4,
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              // 1. عرض الصورة
              Padding(
                padding: const EdgeInsets.all(12),
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    // 2. الاسم واللوكيشن
                    Row(
                      mainAxisAlignment: MainAxisAlignment.spaceBetween,
                      children: [
                        Text(
                          place['name'],
                          style: const TextStyle(
                            fontSize: 18,
                            fontWeight: FontWeight.bold,
                          ),
                        ),
                        IconButton(
                          icon: const Icon(
                            Icons.location_on,
                            color: Colors.red,
                          ),
                          onPressed: () => _openGoogleMap(place['loc']),
                        ),
                      ],
                    ),
                    // 3. نجوم التقييم (نفس تصميم البنات)
                    StatefulBuilder(
                      builder: (context, setState) {
                        return Row(
                          children: [
                            ...List.generate(5, (starIndex) {
                              return GestureDetector(
                                onTap: () {
                                  setState(() {
                                    // هنا نحدث التقييم في بيانات المكان فوراً
                                    place['rating'] = starIndex + 1;
                                  });
                                },
                                child: Icon(
                                  starIndex < (place['rating'] ?? 0)
                                      ? Icons.star
                                      : Icons.star_border,
                                  color: Colors.amber,
                                  size: 28, // حجم أكبر للراحة
                                ),
                              );
                            }),
                            const SizedBox(width: 8),
                            Text(
                              "${place['rating'] ?? 0}.0",
                              style: const TextStyle(
                                fontWeight: FontWeight.bold,
                              ),
                            ),
                          ],
                        );
                      },
                    ),
                    const SizedBox(height: 8),
                    // 4. الوصف
                    Text(
                      place['desc'] ?? '',
                      style: TextStyle(color: Colors.grey[700], fontSize: 14),
                    ),
                  ],
                ),
              ),
            ],
          ),
        );
      },
    );
  }
}

